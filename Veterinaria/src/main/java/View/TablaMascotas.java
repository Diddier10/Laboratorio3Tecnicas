    public void llenarTabla(){
        DefaultTableModel modelo = (DefaultTableModel) this.tabla.getModel();
        List<MascotaDTO> mascotas = new MascotaDAOFile().listarMascotas();
        for (MascotaDTO mascota : mascotas) {
            String[] fila = {
                mascota.getNombre(),
                mascota.getRaza(),
                String.valueOf(mascota.getEdad()),
                mascota.getTipo(),
                mascota.getId(),
                String.valueOf(mascota.getPeso()),
                mascota.getFechaIngreso(),
                mascota.getLugarOrigen(),
                String.valueOf(mascota.getGenero()),
                String.valueOf(mascota.getPrecio()),
                String.valueOf(mascota.isEstadoAdopcion())
            };
            modelo.addRow(fila);
        }
    }

