    public void activarTodo(){
        labelNombre.setEnabled(true);
        nombre.setEnabled(true);
        labelDireccion.setEnabled(true);
        direccion.setEnabled(true);
        labelMascotasACargo.setEnabled(true);
        mascotasACargo.setEnabled(true);
        labelNumeroContacto.setEnabled(true);
        numeroContacto.setEnabled(true);
        labelIdentificacion.setEnabled(true);
        identificacion.setEnabled(true);
    }
    
    public void activarSoloIdentificacion(){
        labelNombre.setEnabled(false);
        nombre.setEnabled(false);
        labelDireccion.setEnabled(false);
        direccion.setEnabled(false);
        labelMascotasACargo.setEnabled(false);
        mascotasACargo.setEnabled(false);
        labelNumeroContacto.setEnabled(false);
        numeroContacto.setEnabled(false);
        labelIdentificacion.setEnabled(true);
        identificacion.setEnabled(true);
    }
