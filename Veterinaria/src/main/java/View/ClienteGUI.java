    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        switch(opcion.getSelectedItem().toString()){
            case "Agregar cliente":
                control.almacenarCliente(identificacion.getText().trim().toUpperCase(), nombre.getText().trim().toUpperCase(),
                        direccion.getText().trim().toUpperCase(), numeroContacto.getText().trim());
                break;
            case "Consultar cliente":
                control.consultarCliente(identificacion.getText().trim());
                break;
            case "Actualizar cliente":
                control.actualizarCliente(identificacion.getText().trim().toUpperCase(), nombre.getText().trim().toUpperCase(),
                        direccion.getText().trim().toUpperCase(), numeroContacto.getText().trim());
                break;
            case "Listar clientes":
                control.listarClientes();
                break;
            case "Eliminar cliente":
                control.eliminarCliente(identificacion.getText().trim());
                break;
        }
    }//GEN-LAST:event_botonAceptarActionPerformed

    private void opcionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_opcionActionPerformed
        switch(opcion.getSelectedItem().toString()){
            case "Agregar cliente":
                activarTodo();                
                break;
            case "Consultar cliente":
                activarSoloIdentificacion();
                break;
            case "Actualizar cliente":
                activarTodo();
                break;
            case "Listar clientes":
                activarSoloIdentificacion();
                labelIdentificacion.setEnabled(false);
                identificacion.setEnabled(false);
                break;
            case "Eliminar cliente":
                activarSoloIdentificacion();
                break;
        }
    }//GEN-LAST:event_opcionActionPerformed

